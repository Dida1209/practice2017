import { geoCoordMap as geoCoordMap, data as data, } from './mapData'

let convertData = function(data) {
    var res = [];
    for (var i = 0; i < data.length; i++) {
        var geoCoord = geoCoordMap[data[i].name];
        if (geoCoord) {
            res.push(geoCoord.concat(data[i].value));
        }
        console.log(res);
    }
    return res;
};

